    private void setOrderQuantity(Integer quantity) {
        // Update value (minimum: 1)
        orderQuantity = quantity < 1 ? 1 : quantity;

        // Update quantity label
        orderQuantityInput.setText(orderQuantity.toString());
        
        // Update total amount
        setTotalAmount();
    }

    private void setOrderItem(Item item) {
        this.orderItem = item;
        
        // Update total amount
        setTotalAmount();
        
        // Update item name and text color for the item name label
        productName.setText(item.getName());
        productName.setForeground(new Color(114,114,114));
    }
    
    private void setTotalAmount() {
        if(orderItem == null) return;
        
        // Total amount = Item price * Quantity
        totalAmountLabel.setText(AppConstants.CURRENCY_FORMAT.format(orderItem.getPrice() * orderQuantity));
    }

    /**
     * Creates new form RecordSaleDialog
     */
    public RecordSaleDialog(JFrame parentFrame, Runnable recordSale) {
        initComponents();
        
        this.parentFrame = parentFrame;
        this.recordSale = recordSale;

        try {
            // Get all existing order IDs using a stream
            List<Integer> orderIds = getAllOrders().stream()
                    .map(Order::getId)
                    .collect(Collectors.toList());

            // Generate the next available ID
            newOrderId = Helpers.generateNextAvailableId(orderIds);
            
            // Update Order ID label
            orderIDLabel.setText(String.format("%03d", newOrderId));
            
        } catch (ApplicationError e) {
            Messages.showError(this, "Application Error", "An unexpected error has occurred while generating a new Order ID");
        }
    }
    
    private void showItemSearchModal() {
        JDialog dialog = new JDialog(parentFrame, "Search Items", true);
        dialog.getContentPane().add(new SearchItemDialog(item -> {
            setOrderItem(item);
            dialog.setVisible(false);
        }));
        dialog.pack();
        dialog.setLocationRelativeTo(parentFrame);
        dialog.setVisible(true);
    }

