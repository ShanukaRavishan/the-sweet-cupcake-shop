    private void orderQuantityInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_orderQuantityInputKeyReleased
        try {
            int qty = Integer.parseInt(orderQuantityInput.getText());
            
            // Minimum allowed order quantity is 1
            if(qty < 1) {
                // Restore previous order quantity
                orderQuantityInput.setText(orderQuantity.toString());
                
                // Display an error message
                Messages.showError(this, "Input Error", "Minimum allowed number of items per order is 1");
                
                return;
            }
            
            // Update the order quantity value
            orderQuantity = qty;
        }

        // If the entered value isn't numeric
        catch(NumberFormatException e) {
            Messages.showError(this, "Input Error", "Please enter a numeric value as the quantity");
            
            // Restore previous order quantity
            orderQuantityInput.setText(orderQuantity.toString());
        }
    }//GEN-LAST:event_orderQuantityInputKeyReleased

    // Event: When the record sale button is clicked
    private void recordSaleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recordSaleBtnActionPerformed
        // If no item is selected to order
        if(orderItem == null) {
            Messages.showError(this, "No Item Found", "No item is selected. Please click the search button and add an item");
            return;
        }
        
        try {
            // Prepare order data to be added
            Order newOrder = new Order(newOrderId, new Date(), orderItem.getName(), orderItem.getPrice(), orderQuantity, orderItem.getPrice() * orderQuantity);
            
            // Add it to the list of orders
            OrderService.saveOrderData(newOrder);
        }
        
        catch(Exception e) {
            Messages.showError(parentFrame, "Application Error", "An unexpected error has been occured while trying to save order data");
            
            e.printStackTrace();
        }

        recordSale.run();
    }//GEN-LAST:event_recordSaleBtnActionPerformed

